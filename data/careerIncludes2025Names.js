/**
 * Players whose `data/csv/career.csv` row already includes 2025 counting stats.
 * When merging, do not add +1 to "seasons" for the 2025 sheet (avoids double-count).
 * Normalization must match server.js `normalizePlayerName`.
 */
function safeText(value) {
  return (value || "").toString().trim();
}

function normalizePlayerName(name) {
  let s = safeText(name).toLowerCase().replace(/[.'’]/g, "");
  s = s.replace(/\s+/g, " ").trim();
  return s;
}

